export const required = value => (value ? undefined : 'Required field');

export const symbols = (value) => {
    const test = /[A-Z\s\*\&\!\@\#\$\%\^\(\)\{\}\[\]\?\'\'\\\/\,\+\-\|\:\;\~\`]/.test(value);
    return (test && 'Wrong label format!') || undefined;
};

export const numbers = (value) => {
    const test = /^-?[0-9]\d*(\d+)?$/.test(value);
    return (!test && 'This field should contain numbers only') || undefined;
};

